// operadores de atribuição

let a = 10;

console.log(`Valor inicial de a: ${a}`);

a += 20;

console.log(`Valor inicial de a: ${a}`);

a *= 5;

console.log(`Valor inicial de a: ${a}`);

a /= 20;

console.log(`Valor inicial de a: ${a}`);

a %= 20;

console.log(`Valor inicial de a: ${a}`);

a **= 20;

console.log(`Valor inicial de a: ${a}`);

